a = 5
b = 1
def add(a, b):
    return a + b
def subtract(a, b):
    return a - b
def multiply(a, b):
    return a * b
def divide(a, b):
    return a / b
def exponent(a, b):
    return a ** b

print("Addition:", add(a, b))
print("Subtraction:", subtract(a, b))       
print("Multiplication:", multiply(a, b))
print("Division:", divide(a, b))
print("Exponent:", exponent(a, b))